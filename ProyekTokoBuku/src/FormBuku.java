    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
String id = txtID.getText();
    String judul = txtJudul.getText();
    String harga = txtHarga.getText();
    String stok = txtStock.getText();
    
    // Memastikan form tidak ada yang kosong saat diklik simpan
    if (id.equals("") || judul.equals("") || harga.equals("") || stok.equals("")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Semua kolom wajib diisi!");
    } else {
        try {
            // Query SQL untuk menyimpan data ke tabel tb_buku
            // 'K001' dimasukkan sementara sebagai contoh id_kategori yang valid
            String sql = "INSERT INTO tb_buku (id_buku, judul_buku, id_kategori, harga, stok) "
                       + "VALUES ('" + id + "', '" + judul + "', 'K001', '" + harga + "', '" + stok + "')";
            
            java.sql.Connection conn = konfigurasi.koneksi.getConfig();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            
            // Notifikasi sukses
            javax.swing.JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!");
            
            // Membersihkan kolom inputan setelah sukses menyimpan
            txtID.setText("");
            txtJudul.setText("");
            txtHarga.setText("");
            txtStock.setText("");
            
            // Memanggil kembali fungsi tampilkanData() agar JTable otomatis terupdate
            tampilkanData();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    String id = txtID.getText();
String judul = txtJudul.getText();
String harga = txtHarga.getText();
String stok = txtStock.getText();

if (id.equals("") || judul.equals("") || harga.equals("") || stok.equals("")) {
    javax.swing.JOptionPane.showMessageDialog(this, "Pilih atau isi data yang ingin diubah!");
} else {
    try {
        // Query SQL untuk mengubah data berdasarkan id_buku
        String sql = "UPDATE tb_buku SET judul_buku='" + judul + "', harga='" + harga + "', stok='" + stok + "' WHERE id_buku='" + id + "'";
        
        java.sql.Connection conn = konfigurasi.koneksi.getConfig();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.execute();
        
        javax.swing.JOptionPane.showMessageDialog(null, "Data Berhasil Diubah!");
        
        // Kosongkan form setelah edit sukses
        txtID.setText("");
        txtJudul.setText("");
        txtHarga.setText("");
        txtStock.setText("");
        
        // Refresh tabel agar data terbaru langsung muncul
        tampilkanData();
        
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengubah data: " + e.getMessage());
    }
}
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
try {
    String html = "<html>"
            + "<head>"
            + "<title>Data Toko Buku</title>"
            + "<style>"
            + "table{border-collapse:collapse;width:100%;}"
            + "th,td{border:1px solid black;padding:8px;text-align:left;}"
            + "th{background:#cccccc;}"
            + "</style>"
            + "</head>"
            + "<body>";
    html += "<h2 align='center'>LAPORAN DATA TOKO BUKU</h2>";
    html += "<table>";
    html += "<tr>"
            + "<th>ID Buku</th>"
            + "<th>Judul Buku</th>"
            + "<th>Harga</th>"
            + "<th>Stok</th>"
            + "</tr>";
            
    for (int i = 0; i < tabelDataBuku.getRowCount(); i++) {
        html += "<tr>";
        for (int j = 0; j < tabelDataBuku.getColumnCount(); j++) {
            html += "<td>"
                    + tabelDataBuku.getValueAt(i, j)
                    + "</td>";
        }
        html += "</tr>";
    }
    html += "</table>";
    html += "</body></html>";
    
    java.io.File file = new java.io.File("laporan_buku.html");
    java.io.FileWriter fw = new java.io.FileWriter(file);
    fw.write(html);
    fw.close();
    
    java.awt.Desktop.getDesktop().browse(file.toURI());
    
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(null,
            "Gagal membuat laporan : "
            + e.getMessage());
}
    }//GEN-LAST:event_btnCetakActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
String id = txtID.getText();

if (id.equals("")) {
    javax.swing.JOptionPane.showMessageDialog(this, "Isi atau pilih ID Buku yang ingin dihapus!");
} else {
    // Memunculkan konfirmasi yakin/tidak sebelum menghapus data
    int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
        try {
            // Query SQL untuk menghapus data berdasarkan id_buku
            String sql = "DELETE FROM tb_buku WHERE id_buku='" + id + "'";
            
            java.sql.Connection conn = konfigurasi.koneksi.getConfig();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            
            javax.swing.JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus!");
            
            // Kosongkan form setelah hapus sukses
            txtID.setText("");
            txtJudul.setText("");
            txtHarga.setText("");
            txtStock.setText("");
            
            // Refresh tabel
            tampilkanData();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
        }
    }
}
    }//GEN-LAST:event_btnHapusActionPerformed

