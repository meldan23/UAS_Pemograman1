    private void tampilkanData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Buku");
        model.addColumn("Judul Buku");
        model.addColumn("Harga");
        model.addColumn("Stok");
        
        try {
            Connection conn = koneksi.getConfig();
            Statement st = conn.createStatement();
            String query = "SELECT * FROM tb_buku";
            ResultSet rs = st.executeQuery(query);
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_buku"),
                    rs.getString("judul_buku"),
                    rs.getString("harga"),
                    rs.getString("stok")
                });
            }
            
            // PASTIKAN nama variabel JTable kamu di tab Design adalah "tabelDataBuku"
            tabelDataBuku.setModel(model); 
            
        } catch (Exception e) {
            System.out.println("Gagal memuat data ke tabel: " + e.getMessage());
        }
    }

